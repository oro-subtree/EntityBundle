<?php

namespace Oro\Bundle\EntityBundle\Fallback\Provider;

abstract class AbstractEntityFallbackProvider implements EntityFallbackProviderInterface
{
    /**
     * @var string
     */
    protected $id;

    /**
     * @return string
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param string $id
     *
     * @return $this
     */
    public function setId($id)
    {
        $this->id = $id;

        return $this;
    }
}
